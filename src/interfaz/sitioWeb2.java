    private void botCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botCarritoActionPerformed
        try {
            int nroProductos=0;
            double precioTot=0,IGVGeneral=0,IGV=0;
            archivos ar=new archivos();
            Cola<Pedido> lista=ar.cargarCompra();
            String Matriz[][]=new String[lista.Contar()][6];
            Nodo<Pedido> a=lista.getFrente();
            for (int i = 0; i < lista.Contar() && a!=null; i++) {
                Matriz[i][0]=a.getDato().getDescrip();
                Matriz[i][1]=a.getDato().getTalla();
                Matriz[i][2]=String.valueOf(a.getDato().getPrecio());
                Matriz[i][3]=String.valueOf(a.getDato().getPrecioTot());
                Matriz[i][4]=String.valueOf(a.getDato().getCantidad());
                Matriz[i][5]=a.getDato().getDeport();
                nroProductos=nroProductos+a.getDato().getCantidad();
                precioTot=precioTot+a.getDato().getPrecioTot();
                IGV = calcularIGV(a.getDato());

                IGVGeneral = IGVGeneral+ IGV;
                a=a.getSiguiente();
            }
            tablaCarrito.setModel(new javax.swing.table.DefaultTableModel(
                Matriz,
                new String [] {
                    "DESCRIPCION", "TALLA", "PRECIO UNITARIO", "PRECIO TOTAL", "CANTIDAD", "DEPORTE"
                }
            ));
            jTextFieldNroCompras.setText(String.valueOf(nroProductos));
            jTextFieldImporte.setText(String.valueOf(precioTot));
            jTextFieldIGV.setText(String.valueOf(IGVGeneral));
            jTextFieldDescuento.setText("10%");
            jTextFieldNeto.setText(String.valueOf(redondearDecimales((precioTot+IGVGeneral)*0.9,2)));
        } catch (IOException ex) {
            //            Logger.getLogger(menuProducto.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            //            Logger.getLogger(menuProducto.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_botCarritoActionPerformed
       
    public  double redondearDecimales(double valorInicial, int numeroDecimales) {
        double parteEntera, resultado;
        resultado = valorInicial;
        parteEntera = Math.floor(resultado);
        resultado=(resultado-parteEntera)*Math.pow(10, numeroDecimales);
        resultado=Math.round(resultado);
        resultado=(resultado/Math.pow(10, numeroDecimales))+parteEntera;
        return resultado;
    }
       
    public double calcularIGV(Pedido aux){
        double igv;
        double igvFinal;
        igv=0.18*aux.getPrecio()*aux.getCantidad();
        igvFinal=redondearDecimales(igv,2);
        return igvFinal;
    }
    
