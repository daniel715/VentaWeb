    private void botonCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCarritoActionPerformed
        try{
        int indice=listaProducto.getSelectedIndex();
        Nodo<Producto> lk=pro.buscar(indice);
        Pedido t=new Pedido();
        for (int i = 0; i < lk.getDato().tallas(); i++) {
            if(Integer.valueOf(textCantidad.getText())>lk.getDato().getStock(i) && listTalla.getSelectedValue().equals(lk.getDato().getTalla(i))){
                JOptionPane.showMessageDialog(this, "Lo sentimos, ha sobrepasado nuestro stock");
            }
            else{
                if(listTalla.getSelectedValue().equals(lk.getDato().getTalla(i))){
                    int sum=lk.getDato().getStock(i)-Integer.parseInt(textCantidad.getText());
                if(sum<10){
                    JOptionPane.showMessageDialog(this, "Lo sentimos, pero alcanzo el limite de nuestro stock.");
                }
                else{
                    t.setDescrip(lk.getDato().getDescrip());
                    t.setTalla(listTalla.getSelectedValue());
                    t.setDeport(lk.getDato().getDeport());
                    int y=0;
                    if(listTalla.getSelectedValue()=="XS"){
                        y=0;
                    }
                    if(listTalla.getSelectedValue()=="S"){
                        y=1;
                    }
                    if(listTalla.getSelectedValue()=="L"){
                        y=2;
                    }
                    if(listTalla.getSelectedValue()=="M"){
                        y=3;
                    }
                    if(listTalla.getSelectedValue()=="XL"){
                        y=4;
                    }
                    if(listTalla.getSelectedValue()=="XXL"){
                        y=5;
                    }
                    t.setPrecio(lk.getDato().getPrecio(y));
                    t.setPrecioTot(lk.getDato().getPrecio(y)*(1.0*Integer.parseInt(textCantidad.getText())));
                    t.setCantidad(Integer.parseInt(textCantidad.getText()));
                    colas.encolar(t);
                    archivos hi=new archivos();
                    hi.guardarCompra(colas);
                    JOptionPane.showMessageDialog(this, "Articulo añadida al carrito");
            }
                }            
        }
        }
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Ingrese datos números donde corresponda.");
        }
    }//GEN-LAST:event_botonCarritoActionPerformed

